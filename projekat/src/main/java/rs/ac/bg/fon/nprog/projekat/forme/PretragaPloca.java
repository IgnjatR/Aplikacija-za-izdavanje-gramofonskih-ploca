    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        if (eId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nijedna ploca nije ucitana", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        eAutorV.setEnabled(true);
        eNazivV.setEnabled(true);
        eCena.setEnabled(true);
        eDatum.setEnabled(true);
        eId.setEnabled(true);
        eBrojPesama.setEnabled(true);
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void eSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eSaveActionPerformed

        if (eId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nijedna ploca nije ucitana", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {

            String autor = eAutorV.getText();
            DateFormat format = new SimpleDateFormat("yyyy-MM-dd");

            Date datumIzdavanja = format.parse(eDatum.getText());

            int brojPesama = Integer.parseInt(eBrojPesama.getText());
            int id = Integer.parseInt(eId.getText());
            BigDecimal cenaIzdavanja = new BigDecimal(eCena.getText());

            String naziv = eNazivV.getText();
            Ploca ploca = new Ploca();
            ploca.setAutor(autor);
            ploca.setNaziv(naziv);
            ploca.setBrojPesama(brojPesama);
            ploca.setCenaIzdavanja(cenaIzdavanja);
            ploca.setDatumIzdavanja(datumIzdavanja);
            ploca.setIdPloce(id);

            KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.IZMENI_PLOCU, ploca);
            Kontroler.getInstance().izmeniPlocu(kz);
            initalState();

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Neispravni podaci", "Greska", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Neispravni podaci", "Greska", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_eSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (eId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nijedna ploca nije ucitana", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Ploca ploca = new Ploca();
        ploca.setIdPloce(Integer.parseInt(eId.getText()));

        KlijentskiZahtev kz = new KlijentskiZahtev(Operacije.IZBRISI_PLOCU, ploca);
        Kontroler.getInstance().izbrisiPlocu(kz);
        initalState();
    }//GEN-LAST:event_btnDeleteActionPerformed

