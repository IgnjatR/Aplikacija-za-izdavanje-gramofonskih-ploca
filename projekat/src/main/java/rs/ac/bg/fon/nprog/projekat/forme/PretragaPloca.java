    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        String naziv = eNaziv.getText();
        String autor = eAutor.getText();

        if (naziv.isEmpty() || autor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti ispravne podatke", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Ploca pl = new Ploca();
        pl.setNaziv(naziv);
        pl.setAutor(autor);
        pl = Kontroler.getInstance().pretraziPlocu(new KlijentskiZahtev(Operacije.PRETRAZI_PLOCE, pl));

        if (pl == null) {
            JOptionPane.showMessageDialog(this, "Ploca nije pronadjena", "Greska", JOptionPane.ERROR_MESSAGE);
            return;
        }
        eAutorV.setText(pl.getAutor());
        eNazivV.setText(pl.getNaziv());
        eCena.setText(pl.getCenaIzdavanja().toString());
        eDatum.setText(pl.getDatumIzdavanja().toString());
        eId.setText(String.valueOf(pl.getIdPloce()));
        eBrojPesama.setText(String.valueOf(pl.getBrojPesama()));


    }//GEN-LAST:event_btnPretraziActionPerformed

